    private void jbtn_grabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_grabarActionPerformed
      
        //capturar datos
        
        String titulo,autor,dia,mes,ano,fechaStr;
        Date fecha;
        int precio;
        boolean disponible;
        
        titulo = this.jtxt_titulo.getText();
        autor = this.jtxt_autor.getText();
        dia = this.jtxt_dia.getText();
        mes = this.jtxt_mes.getText();
        ano = this.jtxt_ano.getText();
        
        fechaStr = dia +"/" + mes + "/" + ano;
        
        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
        try {
            fecha = formato.parse(fechaStr);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al ingresar fecha", "fecha", 2);
            this.jtxt_dia.requestFocus();
            return;
        }
        
        precio = Integer.parseInt(this.jtxt_precio.getText());
        
        disponible = this.jchk_disponible.isSelected(); // 0 o 1 
        
        Libro libro = new Libro(0, titulo, autor, fecha, precio, disponible);
        Registro reg = new Registro();
        reg.agregar(libro);
        JOptionPane.showMessageDialog(null, "Ingreso Libro exitoso!", "Ingreso", 1);
        
    }//GEN-LAST:event_jbtn_grabarActionPerformed

